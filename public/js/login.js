function hideAlert() {
    document.getElementById("alert").className = "alert";
}

/* ═══════════════════════════════
   SUBMIT LOGIN          ← CHANGED
   ═══════════════════════════════ */
async function submitLogin(e) {
    e.preventDefault();
    hideAlert();
    const email = document.getElementById("loginEmail").value.trim();
    const pass = document.getElementById("loginPass").value;
    if (!email || !pass) {
        showAlert(t("err_fields"));
        return;
    }

    const btn = document.getElementById("login-btn");
    btn.classList.add("loading");
    btn.disabled = true;

    try {
        const endpoint =
            selectedRole === "admin"
                ? window.ROUTES.adminLogin
                : window.ROUTES.login;

        const res = await fetch(endpoint, {
            method: "POST",
            headers: {
                "Content-Type": "application/json",
                Accept: "application/json",
            },
            body: JSON.stringify({ email, password: pass }),
        });
        const json = await res.json();

        if (res.ok && json.success) {
            localStorage.setItem("sf_token", json.data.token);
            localStorage.setItem(
                "sf_user",
                JSON.stringify({
                    id: json.data.user.id,
                    name: json.data.user.name,
                    email: json.data.user.email,
                    role: json.data.user.role,
                }),
            );
            showAlert(t("ok_login"), "ok");
            toast(t("ok_login"), "ok");
            const redirect =
                selectedRole === "admin"
                    ? window.ROUTES.adminDashboard
                    : window.ROUTES.dashboard;
            setTimeout(() => {
                window.location.href = redirect;
            }, 1200);
        } else {
            const msg =
                json.message ||
                (selectedRole === "admin" ? t("err_admin") : t("err_login"));
            showAlert(msg);
            document.getElementById("loginPass").classList.add("err-field");
        }
    } catch (err) {
        showAlert("Network error — please try again.");
    } finally {
        btn.classList.remove("loading");
        btn.disabled = false;
    }
}

/* ═══════════════════════════════
   SUBMIT REGISTER       ← CHANGED
   ═══════════════════════════════ */
async function submitRegister(e) {
    e.preventDefault();
    hideAlert();
    const name = document.getElementById("regName").value.trim();
    const email = document.getElementById("regEmail").value.trim();
    const pass = document.getElementById("regPass").value;
    const conf = document.getElementById("regConf").value;
    const agree = document.getElementById("agreeTerms").checked;

    if (!name || !email || !pass || !conf) {
        showAlert(t("err_fields"));
        return;
    }
    if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
        showAlert(t("err_email"));
        return;
    }
    if (pass.length < 8) {
        showAlert(t("err_pass"));
        return;
    }
    if (pass !== conf) {
        showAlert(t("err_match"));
        return;
    }
    if (!agree) {
        showAlert(t("err_terms"));
        return;
    }

    const btn = document.getElementById("reg-btn");
    btn.classList.add("loading");
    btn.disabled = true;

    try {
        const res = await fetch(window.ROUTES.register, {
            method: "POST",
            headers: {
                "Content-Type": "application/json",
                Accept: "application/json",
            },
            body: JSON.stringify({
                name,
                email,
                password: pass,
                password_confirmation: conf,
            }),
        });
        const json = await res.json();

        if (res.ok && json.success) {
            localStorage.setItem("sf_token", json.data.token);
            localStorage.setItem(
                "sf_user",
                JSON.stringify({
                    id: json.data.user.id,
                    name: json.data.user.name,
                    email: json.data.user.email,
                    role: "user",
                }),
            );
            showAlert(t("ok_register"), "ok");
            toast(t("ok_register"), "ok");
            setTimeout(() => {
                window.location.href = window.ROUTES.dashboard;
            }, 1300);
        } else {
            const msg =
                json.message ||
                (json.errors
                    ? Object.values(json.errors)[0][0]
                    : t("err_fields"));
            showAlert(msg);
        }
    } catch (err) {
        showAlert("Network error — please try again.");
    } finally {
        btn.classList.remove("loading");
        btn.disabled = false;
    }
}

